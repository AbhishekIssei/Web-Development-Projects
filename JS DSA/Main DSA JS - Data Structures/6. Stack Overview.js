// Stack

// A stack is a linear data structure that follows the Last In, First Out (LIFO) principle. 
// This means that the last element added to the stack will be the first one to be removed. 
// Stacks are used in various applications, including function call management, 
// expression evaluation, and backtracking algorithms.

// Characteristics of a Stack
// LIFO: Last In, First Out order of operations.
// Operations:
// Push: Add an element to the top of the stack.
// Pop: Remove the top element from the stack.
// Peek/Top: View the top element without removing it.
// isEmpty: Check if the stack is empty.
// Size: Get the number of elements in the stack.
// Clear: Removes all elements from the stack by resetting the array.

// Stacks are used in various applications due to their Last In, First Out (LIFO) nature. 
// Here are some common use cases and examples of how stacks are used:

// 1. Function Call Management
// Stacks are used to manage function calls in programming languages. When a function is called, 
// its execution context is pushed onto the call stack. When the function returns, its execution context is popped from the stack.

// 2. Expression Evaluation
// Stacks are used to evaluate expressions, especially in compilers and interpreters. For example, 
// converting infix expressions (e.g., A + B) to postfix expressions (e.g., AB+) and evaluating them.

// 3. Undo Mechanism
// Text editors and other applications use stacks to implement the undo feature. 
// Each action is pushed onto a stack, and when the user wants to undo an action, the last action is popped from the stack and reversed.

// 4. Backtracking Algorithms
// Stacks are used in algorithms that involve backtracking, 
// such as solving mazes, puzzles, and performing depth-first search (DFS) in graphs.