// Queue

// A queue is a linear data structure that follows the First In, 
// First Out (FIFO) principle. 
// This means that the first element added to the queue will be the first one to be removed. 
// Queues are used in various applications, including task scheduling, managing requests in web servers, 
// and breadth-first search (BFS) in graphs.

// Characteristics of a Queue
// FIFO: First In, First Out order of operations.
// Operations:
// Enqueue: Add an element to the end of the queue.
// Dequeue: Remove the front element from the queue.
// Front/Peek: View the front element without removing it.
// isEmpty: Check if the queue is empty.
// Size: Get the number of elements in the queue.


// Queues are used in various scenarios where the order of processing is important. 
// Here are some common use cases and examples of how queues are used:

// 1. Task Scheduling
// Queues are used to manage tasks in operating systems, 
// ensuring that tasks are processed in the order they arrive.

// 2. Request Handling
// Web servers use queues to manage incoming requests, processing them in the order they are received.

// 3. Breadth-First Search (BFS)
// Queues are used in BFS algorithms to explore nodes level by level in a graph or tree.

// 4. Print Queue
// Printers use queues to manage print jobs, 
// ensuring that documents are printed in the order they are submitted.